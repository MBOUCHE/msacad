                            <h4>Historique de Multisoft Academy</h4>
                            Multisoft Academy&REG; est une Société à Responsabilité Limitée (SARL) exerçant ses principales activités dans le domaine de l’informatique. Elle est créée en décembre 2007 par Registres numéros 438/2006/2007 et 125 du registre chronologique et analytique respectivement basés sur le  Registre global du Commerce et du crédit mobilier du traité OHADA du 17 avril 1997 à Cotonou instituant l’Acte uniforme sur le droit commercial en Afrique. Lors de sa création elle était dirigée par <b>M. Adam Haroun Saleh</b> ; mais depuis janvier 2017 c’est le <b>Dr Jean-Louis FENDJI</b> qui a repris les rênes du centre de formation.
                        </li>
