                            <h4>Secteur d’activité</h4>
                            Il faut noter que, en tant que centre de formation reconnu par l’Etat du Cameroun, ce centre forme dans les filières telles que le <b>Secrétariat Bureautique, le Secrétariat Comptable, la Comptabilité Informatisée et Gestion, le Réseau Informatique et Câblage, la maintenance informatique, Développement  d’application Windows (Basic), Webmestre, Graphisme de Production(DAO) et enfin Maintenance et Réseaux Informatiques . Ensuite, en plus de cela s’ajoutent des formations des formations accélérées et spécialisées telles que : l’Initiation à la Saisie et au Traitement de Texte, l’internet et les applications Web, la Présentation Assistée par Ordinateur par Ms Office PowerPoint, Programmation Excel, Mise en Forme/Finalisation des Mémoires et Rapports Académiques, Cartographie Assistée par Ordinateur</b> et bien d’autres.<br>
                            Depuis 2008, le Centre de formation délivre des attestations de fin de formation dans les filières suscitées pour un total de plus de <b>500 apprenants</b> déjà formés.

