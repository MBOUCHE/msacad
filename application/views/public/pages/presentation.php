                    <h3>Historique et secteur d’activité</h3>

                    <ul>
                        <li>
                            <h4>Historique de Multisoft Academy</h4>
                            Multisoft Academy&REG; est une Société à Responsabilité Limitée (SARL) exerçant ses principales activités dans le domaine de l’informatique. Elle est créée en décembre 2007 par Registres numéros 438/2006/2007 et 125 du registre chronologique et analytique respectivement basés sur le  Registre global du Commerce et du crédit mobilier du traité OHADA du 17 avril 1997 à Cotonou instituant l’Acte uniforme sur le droit commercial en Afrique. Lors de sa création elle était dirigée par <b>M. Adam Haroun Saleh</b> ; mais depuis janvier 2017 c’est le <b>Dr Jean-Louis FENDJI</b> qui a repris les rênes du centre de formation.
                        </li>
                        <li>
                            <h4>Secteur d’activité</h4>
                            En tant que centre de formation reconnu par l’Etat du Cameroun, ce centre forme dans les filières telles que le <b>Secrétariat Bureautique, le Secrétariat Comptable, la Comptabilité Informatisée et Gestion, le Réseau Informatique et Câblage, la maintenance informatique, Développement  d’application Windows (Basic), Webmestre, Graphisme de Production(DAO) et enfin Maintenance et Réseaux Informatiques . </b>Ensuite, en plus de cela s’ajoutent des formations accélérées et spécialisées telles que : <b>l’Initiation à la Saisie et au Traitement de Texte, l’internet et les applications Web, la Présentation Assistée par Ordinateur par Ms Office PowerPoint, Programmation Excel, Mise en Forme/Finalisation des Mémoires et Rapports Académiques, Cartographie Assistée par Ordinateur</b> et bien d’autres.<br>
                            Depuis 2008, le Centre de formation délivre des attestations de fin de formation dans les filières suscitées pour un total de plus de <b>500 apprenants</b> déjà formés.

                        </li>
